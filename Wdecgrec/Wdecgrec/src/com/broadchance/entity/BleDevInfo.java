package com.broadchance.entity;

public class BleDevInfo {
	private String devInfoName;
	private String devInfoValue;
	public BleDevInfo(){
		
	}
	public String getDevInfoName() {
		return devInfoName;
	}
	public void setDevInfoName(String devInfoName) {
		this.devInfoName = devInfoName;
	}
	public String getDevInfoValue() {
		return devInfoValue;
	}
	public void setDevInfoValue(String devInfoValue) {
		this.devInfoValue = devInfoValue;
	}

}
