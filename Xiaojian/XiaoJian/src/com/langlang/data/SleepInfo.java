package com.langlang.data;

public class SleepInfo {
	private String start_time;//开始世间
	private String maintain_time;//维持时间
	private String sleep_state;//睡眠状态

	public String getStart_time() {
		return start_time;
	}

	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}

	public String getMaintain_time() {
		return maintain_time;
	}

	public void setMaintain_time(String maintain_time) {
		this.maintain_time = maintain_time;
	}

	public String getSleep_state() {
		return sleep_state;
	}

	public void setSleep_state(String sleep_state) {
		this.sleep_state = sleep_state;
	}

}
